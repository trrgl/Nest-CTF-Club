import time
import random

now = time.time()
now = int(now) // 4
random.seed(now)
flag = b"nest{<REDACTED>}"
key = []
result = []
for _ in flag:
    key.append(random.getrandbits(8))

for i in range(len(flag)):
    result.append(hex(flag[i] ^ key[i]))

print(result)

